def translate(text):
    return translations.get(text, text)

def modify(obj):
    if isinstance(obj, dict):
        for key, value in obj.items():
            if key == 'text' and isinstance(value, str):
                obj[key] = {"ru": value, "kz": translate(value)}
            else:
                modify(value)
    elif isinstance(obj, list):
        for item in obj:
            modify(item)

